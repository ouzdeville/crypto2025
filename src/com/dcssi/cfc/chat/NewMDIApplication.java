    public static void ajouterMessage(String message) {
        textArea.append(message + "\n");
    }

    private void afficherMessages(Correspondant c) {

        textArea.setText(""); // vider l'écran

        for (String msg : c.messages) {
            ajouterMessage(msg);
        }
    }

    public static void addCorrespondant(Correspondant correspondant) {
        NewMDIApplication.correspondantList.add(correspondant);
        DefaultTableModel model = (DefaultTableModel) jTabCorrespondant.getModel();
        boolean existe = false;

        for (int i = 0; i < model.getRowCount(); i++) {
            if (model.getValueAt(i, 0).equals(correspondant.id)) {
                existe = true;
                break;
            }
        }

        if (!existe) {
            model.addRow(new Object[]{
                correspondant,
                correspondant.socket.getInetAddress().getHostAddress(),
                "Connecté"
            });
        }

    }

