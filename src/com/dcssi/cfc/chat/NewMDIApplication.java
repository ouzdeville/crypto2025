    private void openMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openMenuItemActionPerformed
        // TODO add your handling code here:
        JTextField ipField = new JTextField("127.0.0.1");
        JTextField portField = new JTextField("2026");

        Object[] message = {
            "Adresse IP :", ipField,
            "Port :", portField
        };

        int ipOption = JOptionPane.showConfirmDialog(
                null,
                message,
                "Connexion au serveur",
                JOptionPane.OK_CANCEL_OPTION
        );

        if (ipOption == JOptionPane.OK_OPTION) {
            String ip = ipField.getText();
            String portStr = portField.getText();

            try {
                int port = Integer.parseInt(portStr);
                System.out.println("IP = " + ip);
                System.out.println("Port = " + port);

                // Exemple connexion socket
                // Socket s = new Socket(ip, port);
                Socket s = new Socket(ip, port);

                System.out.println("Serveur trouver !");
                JPasswordField passwordField = new JPasswordField();

                int passOption = JOptionPane.showConfirmDialog(
                        null,
                        passwordField,
                        "Entrez votre mot de passe",
                        JOptionPane.OK_CANCEL_OPTION,
                        JOptionPane.PLAIN_MESSAGE
                );

                if (passOption == JOptionPane.OK_OPTION) {
                    char[] pwd = passwordField.getPassword();
                    String password = new String(pwd);

                    Correspondant correspondant = new Correspondant(this.login, s, false, password);
                    NewMDIApplication.addCorrespondant(correspondant);
                    new Recepteur(s, correspondant.id, correspondant.getPassword(), correspondant,this).start();
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null,
                        "Port invalide",
                        "Erreur",
                        JOptionPane.ERROR_MESSAGE);
            } catch (IOException ex) {
                Logger.getLogger(NewMDIApplication.class.getName()).log(Level.SEVERE, null, ex);
            } catch (Exception ex) {
                Logger.getLogger(NewMDIApplication.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_openMenuItemActionPerformed

